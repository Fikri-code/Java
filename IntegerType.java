public class IntegerType {
  public static void main(String[] args) {
    int angka1 = 11;
    short angka2 = 14;
    long angka3 = 2001;
    byte angka4 = 8;

    System.out.println("int = "+ angka1);
    System.out.println("short = "+ angka2);
    System.out.println("long = "+ angka3);
    System.out.println("byte = "+ angka4);
  }
}